-- Spatial domain baseline for the smart agriculture platform.
-- This file is intentionally additive: it keeps crm.agri_feature as the
-- shared spatial index entry and moves strong business fields into detail
-- tables.

CREATE EXTENSION IF NOT EXISTS postgis;
CREATE SCHEMA IF NOT EXISTS crm;

-- ---------------------------------------------------------------------------
-- Shared agri_feature upgrades
-- ---------------------------------------------------------------------------

ALTER TABLE IF EXISTS crm.agri_feature
  ADD COLUMN IF NOT EXISTS farm_id bigint,
  ADD COLUMN IF NOT EXISTS parcel_id bigint,
  ADD COLUMN IF NOT EXISTS owner_org_id bigint,
  ADD COLUMN IF NOT EXISTS bbox geometry(Polygon, 4326),
  ADD COLUMN IF NOT EXISTS created_by bigint,
  ADD COLUMN IF NOT EXISTS updated_by bigint;

CREATE INDEX IF NOT EXISTS idx_agri_feature_farm_type_status
  ON crm.agri_feature (farm_id, feature_type, status);

CREATE INDEX IF NOT EXISTS idx_agri_feature_parcel_id
  ON crm.agri_feature (parcel_id);

CREATE INDEX IF NOT EXISTS idx_agri_feature_bbox
  ON crm.agri_feature USING gist (bbox);

-- ---------------------------------------------------------------------------
-- Organization and management scope
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS crm.organization (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  parent_id bigint NULL REFERENCES crm.organization(id) ON DELETE SET NULL,
  org_code text NOT NULL UNIQUE,
  org_name text NOT NULL,
  org_type text NOT NULL,
  level_no integer NOT NULL DEFAULT 1,
  path text NULL,
  status text NOT NULL DEFAULT 'active',
  attrs jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_organization_parent_id
  ON crm.organization (parent_id);

CREATE TABLE IF NOT EXISTS crm.farm (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  organization_id bigint NULL REFERENCES crm.organization(id) ON DELETE SET NULL,
  farm_code text NOT NULL UNIQUE,
  farm_name text NOT NULL,
  manager_employee_id integer NULL REFERENCES crm."Employee"(id) ON DELETE SET NULL,
  status text NOT NULL DEFAULT 'active',
  geom geometry(MultiPolygon, 4326) NULL,
  centroid_geom geometry(Point, 4326) NULL,
  bbox geometry(Polygon, 4326) NULL,
  area_m2 double precision NULL,
  attrs jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_farm_organization_id
  ON crm.farm (organization_id);

CREATE INDEX IF NOT EXISTS idx_farm_geom
  ON crm.farm USING gist (geom);

CREATE INDEX IF NOT EXISTS idx_farm_bbox
  ON crm.farm USING gist (bbox);

CREATE TABLE IF NOT EXISTS crm.farm_member (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  farm_id bigint NOT NULL REFERENCES crm.farm(id) ON DELETE CASCADE,
  employee_id integer NOT NULL REFERENCES crm."Employee"(id) ON DELETE CASCADE,
  member_role text NOT NULL,
  is_primary boolean NOT NULL DEFAULT false,
  joined_at timestamptz NOT NULL DEFAULT now(),
  left_at timestamptz NULL,
  attrs jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT uk_farm_member_unique UNIQUE (farm_id, employee_id, member_role)
);

CREATE INDEX IF NOT EXISTS idx_farm_member_farm_id
  ON crm.farm_member (farm_id);

CREATE INDEX IF NOT EXISTS idx_farm_member_employee_id
  ON crm.farm_member (employee_id);

-- ---------------------------------------------------------------------------
-- Base spatial objects
