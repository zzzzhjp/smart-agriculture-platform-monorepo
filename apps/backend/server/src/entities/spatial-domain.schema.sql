CREATE TABLE IF NOT EXISTS crm.parcel (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  feature_id bigint NULL UNIQUE REFERENCES crm.agri_feature(id) ON DELETE SET NULL,
  parcel_code text NOT NULL UNIQUE,
  parcel_name text NOT NULL,
  farm_id bigint NOT NULL REFERENCES crm.farm(id) ON DELETE CASCADE,
  owner_org_id bigint NULL REFERENCES crm.organization(id) ON DELETE SET NULL,
  manager_employee_id integer NULL REFERENCES crm."Employee"(id) ON DELETE SET NULL,
  current_crop_id bigint NULL,
  land_use_type text NULL,
  soil_type text NULL,
  irrigation_type text NULL,
  cultivation_status text NULL,
  geom geometry(MultiPolygon, 4326) NOT NULL,
  centroid_geom geometry(Point, 4326) NULL,
  bbox geometry(Polygon, 4326) NULL,
  area_m2 double precision NULL,
  perimeter_m double precision NULL,
  elevation_avg_m double precision NULL,
  slope_avg double precision NULL,
  aspect_main double precision NULL,
  latest_ndvi_dataset_id bigint NULL,
  latest_flood_result_id bigint NULL,
  latest_uav_mission_id bigint NULL,
  valid_from timestamptz NULL,
  valid_to timestamptz NULL,
  is_active boolean NOT NULL DEFAULT true,
  version_no integer NOT NULL DEFAULT 1,
  attrs jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_parcel_farm_id
  ON crm.parcel (farm_id);

CREATE INDEX IF NOT EXISTS idx_parcel_owner_org_id
  ON crm.parcel (owner_org_id);

CREATE INDEX IF NOT EXISTS idx_parcel_active_valid
  ON crm.parcel (is_active, valid_from DESC, valid_to DESC);

CREATE INDEX IF NOT EXISTS idx_parcel_geom
  ON crm.parcel USING gist (geom);

CREATE INDEX IF NOT EXISTS idx_parcel_bbox
  ON crm.parcel USING gist (bbox);

CREATE INDEX IF NOT EXISTS idx_parcel_attrs
