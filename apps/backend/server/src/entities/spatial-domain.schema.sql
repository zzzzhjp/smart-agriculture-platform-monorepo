CREATE OR REPLACE VIEW crm.v_cesium_feature_feed AS
SELECT
  p.feature_id,
  'parcel'::text AS source_table,
  p.id AS source_id,
  'parcel'::text AS feature_type,
  p.farm_id,
  p.id AS parcel_id,
  p.parcel_name AS display_name,
  p.geom,
  p.valid_from,
  p.valid_to,
  NULL::text AS resource_uri,
  jsonb_build_object(
    'versionNo', p.version_no,
    'landUseType', p.land_use_type,
    'cultivationStatus', p.cultivation_status
  ) || p.attrs AS attrs
FROM crm.parcel p

UNION ALL

SELECT
  f.feature_id,
  'facility'::text,
  f.id,
  f.facility_type,
  f.farm_id,
  f.parcel_id,
  f.facility_name,
  f.geom,
  NULL::timestamptz,
  NULL::timestamptz,
  COALESCE(f.tileset_uri, f.model_uri) AS resource_uri,
  jsonb_build_object('thumbnailUri', f.thumbnail_uri) || f.attrs
FROM crm.facility f

UNION ALL

SELECT
  l.feature_id,
  'linear_asset'::text,
  l.id,
  l.asset_type,
  l.farm_id,
  l.parcel_id,
  l.asset_name,
  l.geom,
  NULL::timestamptz,
  NULL::timestamptz,
  NULL::text,
  jsonb_build_object('lengthM', l.length_m) || l.attrs
FROM crm.linear_asset l

UNION ALL

SELECT
  w.feature_id,
  'water_body'::text,
  w.id,
  w.water_body_type,
  w.farm_id,
  w.parcel_id,
  w.water_body_name,
  w.geom,
  NULL::timestamptz,
  NULL::timestamptz,
  NULL::text,
  jsonb_build_object('areaM2', w.area_m2, 'avgDepthM', w.avg_depth_m) || w.attrs
FROM crm.water_body w

UNION ALL

SELECT
  u.feature_id,
  'uav_mission'::text,
  u.id,
  u.mission_type,
  u.farm_id,
  u.parcel_id,
  u.mission_name,
  u.flight_path::geometry,
  u.started_at,
  u.ended_at,
  u.result_uri,
  jsonb_build_object('status', u.status, 'uavSn', u.uav_sn) || u.attrs
FROM crm.uav_mission u

UNION ALL

SELECT
  n.feature_id,
  'ndvi_dataset'::text,
  n.id,
  'ndvi_dataset'::text,
  n.farm_id,
  n.parcel_id,
  n.dataset_name,
  n.preview_geom::geometry,
  n.valid_from,
  n.valid_to,
  n.raster_uri,
  jsonb_build_object(
    'acquiredAt', n.acquired_at,
    'ndviMin', n.ndvi_min,
    'ndviMax', n.ndvi_max,
    'ndviAvg', n.ndvi_avg,
    'pointCloudUri', n.point_cloud_uri,
    'thumbnailUri', n.thumbnail_uri
  ) || n.stats
FROM crm.ndvi_dataset n

UNION ALL

SELECT
  fr.feature_id,
  'flood_analysis_result'::text,
  fr.id,
  'flood_result'::text,
  fr.farm_id,
  fr.parcel_id,
  fr.result_name,
  fr.flood_geom::geometry,
  fr.analysis_time,
  NULL::timestamptz,
  fr.result_uri,
  jsonb_build_object(
    'waterLevelM', fr.water_level_m,
    'inundatedAreaM2', fr.inundated_area_m2
  ) || fr.summary
FROM crm.flood_analysis_result fr;
